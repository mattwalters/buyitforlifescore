import fs from "fs";

function makeCase(
  pipeline,
  id,
  input,
  expectedIssues,
  matchQuote,
  existingComments,
  readableContext,
) {
  const c = { id, pipeline, input };
  const ctx = {};
  if (existingComments) ctx.existingComments = existingComments;
  if (readableContext) {
    if (pipeline === "pov") ctx.readablePOV = readableContext;
    if (pipeline === "tense") ctx.readableTense = readableContext;
  }
  if (Object.keys(ctx).length > 0) c.context = ctx;

  c.expectations = { shouldHaveIssues: expectedIssues };
  if (matchQuote) c.expectations.matchQuote = matchQuote;
  return c;
}

